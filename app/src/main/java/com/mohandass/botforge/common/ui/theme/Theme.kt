@Composable
fun BotForgeTheme(
    appViewModel: AppViewModel = hiltViewModel(),
    content: @Composable () -> Unit,
) {
    var useDynamicColor = true
    var useDarkTheme = isSystemInDarkTheme()
    val userPreferences = appViewModel.appState.userPreferences.observeAsState()

    userPreferences.value?.let {
        useDarkTheme = when (it.preferredTheme) {
            PreferredTheme.LIGHT -> false
            PreferredTheme.DARK -> true
            PreferredTheme.AUTO -> isSystemInDarkTheme()
        }

        useDynamicColor = it.useDynamicColors
    }

    val colorScheme = when {
        useDynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (useDarkTheme) dynamicDarkColorScheme(context)
            else dynamicLightColorScheme(context)
        }

        useDarkTheme -> DarkColors
        else -> LightColors
    }

    val view = LocalView.current

    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            val insets = WindowCompat.getInsetsController(window, view)
            window.statusBarColor = Color.Transparent.toArgb()
            window.navigationBarColor = Color.Transparent.toArgb()
            insets.isAppearanceLightStatusBars = !useDarkTheme
            insets.isAppearanceLightNavigationBars = !useDarkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = Typography,
        content = content,
    )
}

